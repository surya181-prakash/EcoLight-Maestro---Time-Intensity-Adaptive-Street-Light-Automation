//DEFINES.H
#define SETBIT(WORD,BIT)   WORD=WORD|(1<<BIT)
#define CLRBIT(WORD,BIT)   WORD=WORD&(~(1<<BIT))
#define CPLBIT(WORD,BIT)   WORD=WORD^(1<<BIT)
#define WRITEBIT(WORD,BITPOS,BIT) WORD=(WORD&(~(1<<BITPOS)))|(BIT<<BITPOS)
#define WRITEBYTE(WORD,BITPOS,BIT) WORD=(WORD&(~(0XFF<<BITPOS)))|(BIT<<BITPOS)
#define READBIT(WORD,BIT)   ((WORD>>BIT)&1)
#define READNIBBLE(WORD,BIT) ((WORD>>BIT)&15)
#define READBYTE(WORD,BIT)  ((WORD>>BIT)&255)
#define WRITENIBBLE(WORD,BITPOS,VAL) WORD=((WORD&~(15<<BITPOS))|(VAL<<BITPOS))
